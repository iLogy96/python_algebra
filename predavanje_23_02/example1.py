a = int(input('Input first number: ')  ) 
b = int(input('Input second number: '))

sum = a + b
difference = a - b
multiply = a * b
divide = a / b
exponent = a ** b
modulo = a % b

print(a, '+', b, '=', sum)
print(a, '-', b, '=', difference)
print(a, '*', b, '=', multiply)
print(a, '/', b, '=', divide)
print(a, '**', b, '=', exponent)
print(a, '%', b, '=', modulo)
